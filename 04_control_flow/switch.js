const month = 3

switch(month){
    case 1:
        console.log("January")
        break
    case 2:
        console.log("February")
        break
    case 3:
        console.log("March")
        break
    case 4:
        console.log("April")
        break
    default:
        console.log("Invalid month")
}

// switch statement is used to compare the value of a variable to multiple values. break statement is used to exit the switch statement. If break is not used, the code will continue to execute the next case except default. If none of the cases match, the default case will run.
